export function getActiveQiStage(hour: number): string {
  if (hour >= 23 || hour < 1) return "STILLNESS";
  if (hour < 5) return "DARKNESS";
  if (hour < 7) return "WIND";
  if (hour < 9) return "HUMIDITY";
  if (hour < 13) return "HEAT";
  if (hour < 17) return "WARMNESS";
  if (hour < 19) return "COLDNESS";
  return "DRYNESS";
}
